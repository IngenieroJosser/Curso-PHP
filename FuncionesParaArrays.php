    /*
PHP ofrece una amplia variedad de funciones para trabajar con arrays. 
Estas funciones facilitan la manipulación, búsqueda, ordenamiento y procesamiento de datos en arrays. 
Aquí tienes algunos ejemplos de funciones comunes para arrays en PHP:

**Creación y Modificación de Arrays:**
1. `array()` - Crea un nuevo array.
2. `count()` - Devuelve la cantidad de elementos en un array.
3. `array_push()` - Añade uno o más elementos al final de un array.
4. `array_pop()` - Elimina y devuelve el último elemento de un array.
5. `array_unshift()` - Añade uno o más elementos al principio de un array.
6. `array_shift()` - Elimina y devuelve el primer elemento de un array.
7. `array_merge()` - Combina dos o más arrays en uno.

**Acceso y Búsqueda en Arrays:**
8. `in_array()` - Comprueba si un valor existe en un array.
9. `array_key_exists()` - Comprueba si una clave existe en un array.
10. `array_search()` - Busca un valor en un array y devuelve la clave correspondiente.
11. `array_values()` - Devuelve todos los valores de un array como un nuevo array.

**Ordenamiento de Arrays:**
12. `sort()` - Ordena un array en orden ascendente.
13. `rsort()` - Ordena un array en orden descendente.
14. `asort()` - Ordena un array asociativo en orden ascendente según los valores.
15. `arsort()` - Ordena un array asociativo en orden descendente según los valores.
16. `ksort()` - Ordena un array asociativo en orden ascendente según las claves.
17. `krsort()` - Ordena un array asociativo en orden descendente según las claves.

**Extracción y Segmentación de Arrays:**
18. `array_slice()` - Extrae una porción de un array.
19. `array_splice()` - Elimina y reemplaza una porción de un array.
20. `array_chunk()` - Divide un array en fragmentos de un tamaño específico.

**Manipulación de Elementos:**
21. `array_map()` - Aplica una función a cada elemento de un array y devuelve un nuevo array.
22. `array_filter()` - Filtra elementos de un array basándose en una función de retorno.
23. `array_reduce()` - Reduce los elementos de un array a un solo valor utilizando una función de retorno.

**Otras Funciones Útiles:**
24. `array_reverse()` - Invierte el orden de los elementos en un array.
25. `array_unique()` - Elimina elementos duplicados de un array.
26. `array_rand()` - Devuelve una o más claves aleatorias de un array.

Estos son solo algunos ejemplos de funciones para arrays en PHP. 
PHP proporciona una amplia gama de funciones de manipulación de arrays que pueden ser muy útiles en diversas situaciones. 
